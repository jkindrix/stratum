/**
 * Reverse lookup: Forte name to prime form.
 * Includes both canonical names and Z-stripped names for convenience.
 */
export const FORTE_BY_NAME: Readonly<Record<string, readonly number[]>> = (() => {
  const result: Record<string, readonly number[]> = {};
  for (const [key, entry] of Object.entries(FORTE_CATALOG)) {
    const pf = Object.freeze(key.split(',').map(Number));
    result[entry.name] = pf;
    // Also allow lookup without Z prefix (e.g., '4-15' → same as '4-Z15')
    const stripped = entry.name.replace('Z', '');
    if (stripped !== entry.name) {
      result[stripped] = pf;
    }
  }
  return result;
})();
